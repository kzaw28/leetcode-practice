class MyStack:

    def __init__(self):
        self.q = deque()

    def push(self, x: int) -> None:
        self.q.append(x)

    def pop(self) -> int:
        # Move elements to the back until the last one is at the front
        for _ in range(len(self.q) - 1):
            self.push(self.q.popleft())
        # Pop that one
        return self.q.popleft()

    def top(self) -> int:
        return self.q[-1]

    def empty(self) -> bool:
        return len(self.q) == 0


